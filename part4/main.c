        static void build_sine(void)
        {
            const float mid = 2048.0f;
            const float amp = 2047.0f * 0.90f;

            for (uint32_t i = 0; i < LUT_SIZE; ++i) {
                float theta = (2.0f * (float)M_PI * (float)i) / (float)LUT_SIZE;
                int32_t v   = (int32_t)lroundf(mid + amp * sinf(theta));
                if (v < 0)     v = 0;
                if (v > 4095)  v = 4095;
                sine_lut[i] = (uint16_t)v;
            }
        }

        // "Fat" sine with a few harmonics mixed in (still clipped to 12-bit)
        static void build_flut(void)
        {
            const float mid = 2048.0f;
            const float amp = 2047.0f * 0.90f;

            // tweak these to taste
            const float h2 = 0.50f;   // 2nd harmonic level
            const float h3 = 0.25f;   // 3rd
            const float h4 = 0.125f;  // 4th

            for (uint32_t i = 0; i < LUT_SIZE; ++i) {
                float theta = (2.0f * (float)M_PI * (float)i) / (float)LUT_SIZE;

                float wave =
                    sinf(theta) +
                    h2 * sinf(2.0f * theta) +
                    h3 * sinf(3.0f * theta) +
                    h4 * sinf(4.0f * theta);

                int32_t v = (int32_t)lroundf(mid + (amp * 0.5f) * wave);
                if (v < 0)     v = 0;
                if (v > 4095)  v = 4095;
                sine_lut[i] = (uint16_t)v;
            }
        }
